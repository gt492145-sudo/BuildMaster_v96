    function normalizeLang(raw) {
        const code = String(raw || '').trim().toLowerCase().replace(/_/g, '-');
        if (!code) return 'zh-TW';
        if (code === 'ja' || code.startsWith('ja-')) return 'ja';
        if (code === 'en' || code.startsWith('en-')) return 'en';
        if (code === 'zh-tw' || code === 'zh-hant' || code.startsWith('zh')) return 'zh-TW';
        return 'zh-TW';
    }

    function resolveLang() {
        try {
            const params = new URLSearchParams(global.location && global.location.search ? global.location.search : '');
            const fromUrl = params.get('lang');
            if (fromUrl) return normalizeLang(fromUrl);
        } catch (_e) {}
        try {
            const nav = global.navigator;
            const list = nav && nav.languages && nav.languages.length ? nav.languages : [nav && nav.language].filter(Boolean);
            for (let i = 0; i < list.length; i += 1) {
                const lang = normalizeLang(list[i]);
                if (LOCALES[lang]) return lang;
            }
        } catch (_e2) {}
        return 'zh-TW';
    }

    const activeLang = resolveLang();

    function interpolate(text, vars) {
        if (!vars || typeof text !== 'string') return text;
        return text.replace(/\{(\w+)\}/g, (_, key) => (vars[key] != null ? String(vars[key]) : `{${key}}`));
    }

    function t(key, vars) {
        const bucket = LOCALES[activeLang] || LOCALES['zh-TW'];
        const fallback = LOCALES['zh-TW'];
        const raw = bucket[key] != null ? bucket[key] : (fallback[key] != null ? fallback[key] : key);
        return interpolate(raw, vars);
    }

    function localeTag() {
        if (activeLang === 'ja') return 'ja-JP';
        if (activeLang === 'en') return 'en-US';
        return 'zh-TW';
    }

    function applyDomI18n(root) {
        const scope = root || global.document;
        if (!scope || !scope.querySelectorAll) return;
        scope.querySelectorAll('[data-i18n]').forEach((el) => {
            el.textContent = t(el.getAttribute('data-i18n'));
        });
        scope.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
            el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
        });
        scope.querySelectorAll('[data-i18n-title]').forEach((el) => {
            el.title = t(el.getAttribute('data-i18n-title'));
        });
        scope.querySelectorAll('[data-i18n-aria]').forEach((el) => {
            el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
        });
        scope.querySelectorAll('[data-i18n-label]').forEach((el) => {
            const key = el.getAttribute('data-i18n-label');
            if (el.tagName === 'OPTGROUP') el.label = t(key);
            else el.textContent = t(key);
        });
        scope.querySelectorAll('option[data-i18n]').forEach((el) => {
            el.textContent = t(el.getAttribute('data-i18n'));
        });
    }

    function applyMetaI18n() {
        try {
            global.document.title = t('meta.title');
            const html = global.document.documentElement;
            if (html) html.setAttribute('lang', t('meta.htmlLang'));
        } catch (_e) {}
    }

    function refreshFooterLabels() {
        const pairs = [
            ['sumC', 'footer.concrete', '0'],
            ['sumM', 'footer.formwork', '0'],
            ['sumE', 'footer.earth', '0'],
            ['sumS', 'footer.rebar', '0']
        ];
        pairs.forEach(([id, key, fallback]) => {
            const el = global.document.getElementById(id);
            if (!el) return;
            const match = String(el.textContent || '').match(/[\d,.]+/);
            const n = match ? match[0] : fallback;
            el.textContent = t(key, { n });
        });
    }

    function bootI18n() {
        applyMetaI18n();
        applyDomI18n(global.document);
        refreshFooterLabels();
    }

    function registerLocaleKeys(bucket) {
        if (!bucket || typeof bucket !== 'object') return;
        Object.keys(bucket).forEach((lang) => {
            if (!LOCALES[lang]) LOCALES[lang] = {};
            Object.assign(LOCALES[lang], bucket[lang]);
        });
    }

    global.BM_I18N = {
        lang: activeLang,
        t,
        localeTag,
        applyDomI18n,
        refreshFooterLabels,
        bootI18n,
        registerLocaleKeys
    };
    global.BM_T = t;
    global.BM_LOCALE = localeTag;

    if (global.document) {
        if (global.document.readyState === 'loading') {
            global.document.addEventListener('DOMContentLoaded', bootI18n);
        } else {
            bootI18n();
        }
    }
